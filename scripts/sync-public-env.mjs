import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const explicitTarget = process.argv[2];
const targetEnv = (explicitTarget
  ?? process.env['VERCEL_TARGET_ENV']
  ?? process.env['VERCEL_ENV']
  ?? process.env['NODE_ENV']
  ?? 'development').toLowerCase();

const envFileCandidates = targetEnv === 'production'
  ? ['.env.production.local', '.env.local']
  : targetEnv === 'preview'
    ? ['.env.preview.local', '.env.local']
    : ['.env.local', '.env.preview.local', '.env.production.local'];

function parseEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return {};

  const parsed = {};
  const lines = fs.readFileSync(filePath, 'utf8').split(/\r?\n/);

  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;

    const match = /^([A-Z0-9_]+)=(.*)$/.exec(line);
    if (!match) continue;

    let [, key, value] = match;
    value = value.trim();

    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith('\'') && value.endsWith('\''))
    ) {
      value = value.slice(1, -1);
    }

    parsed[key] = value
      .replace(/\\n/g, '\n')
      .replace(/\\r/g, '\r')
      .replace(/\\"/g, '"');
  }

  return parsed;
}

const fileEnv = envFileCandidates.reduce((acc, filename) => {
  const fullPath = path.join(root, filename);
  return { ...acc, ...parseEnvFile(fullPath) };
}, {});

const env = {
  ...fileEnv,
  GOOGLE_MAPS_API_KEY: process.env['GOOGLE_MAPS_API_KEY'] ?? fileEnv['GOOGLE_MAPS_API_KEY'] ?? '',
  SENTRY_DSN: process.env['SENTRY_DSN'] ?? fileEnv['SENTRY_DSN'] ?? '',
};

const outputPath = path.join(root, 'src', 'environments', 'public-env.generated.ts');
const content = `// Auto-generated by scripts/sync-public-env.mjs
// Do not edit manually.
export const publicEnv = {
  googleMapsApiKey: ${JSON.stringify(env['GOOGLE_MAPS_API_KEY'] ?? '')},
  sentryDsn: ${JSON.stringify(env['SENTRY_DSN'] ?? '')},
} as const;
`;

fs.writeFileSync(outputPath, content, 'utf8');
console.log(`[sync-public-env] Wrote ${path.relative(root, outputPath)} for ${targetEnv}`);
